package Util;

/**
 * 说 明：坐标位置(x,y)
 * Created by wuzhu on 2017/3/13.
 */
public class POS {
    public int x; //横向x坐标
    public int y; //竖向y坐标
    public POS(int x,int y){
        this.x = x;
        this.y = y;
    }

    public void setPOS(int x,int y){
        this.x = x;
        this.y = y;
    }
}
